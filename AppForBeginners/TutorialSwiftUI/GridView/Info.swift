///Information
///Grid View:-
///view is a container view that arranges its children in a grid layout
///There are primarily two types of grids: LazyVGrid and LazyHGrid
///
/*
Here's a more detailed explanation:

LazyVGrid:

LazyVGrid arranges its items in a vertical grid layout.
You can define the number of columns, their spacing, alignment, and more.
It's called "lazy" because it only creates views for the items that are currently visible on the screen, making it efficient for large data sets.
LazyHGrid:

LazyHGrid arranges its items in a horizontal grid layout.
Like LazyVGrid, you can define the number of rows, their spacing, alignment, etc.
It's also lazy, meaning it only creates views for the items currently visible on the screen, which helps with performance.*/

